"use client";

import { useState } from "react";
import { Plus, Minus, Dot } from "lucide-react";

export default function WhoWeServe() {
  const [openAccordion, setOpenAccordion] = useState<number | null>(0);

  const toggleAccordion = (index: number) => {
    setOpenAccordion(openAccordion === index ? null : index);
  };

  const targetAudiences = [
    {
      title: "Transaction Intelligence & Risk Signals",
      description:
        "We design systems that analyze transaction data in real time to detect patterns, risks, and anomalies. By embedding intelligence into payment and financial workflows, organizations improve authorization accuracy, reduce false positives, and strengthen overall risk management.",
    },
    {
      title: "Financial Process Automation",
      description:
        "We engineer automation across onboarding, reconciliation, reporting, and compliance workflows. This reduces manual effort, improves processing speed, and allows teams to scale financial operations while maintaining accuracy and auditability.",
    },
    {
      title: "Fraud Detection & Predictive Controls",
      description:
        "We build data and analytics foundations that enable proactive financial protection. This includes fraud detection, behavior modeling, and predictive controls that help organizations identify threats early, reduce losses, and adapt defenses as transaction patterns evolve.",
    },
  ];

  return (
    <section className="py-16 bg-white">
      <div className="max-w-7xl mx-auto px-6 lg:px-12 xl:px-16">
        <div className="grid md:grid-cols-2 gap-12">
          {/* Left Side - Main Heading */}
          <div>
            <h2 className="text-3xl md:text-2xl lg:text-[26px] font-bold text-gray-900 leading-tight mb-6">
              At scale, financial operations require continuous risk assessment,
              transaction monitoring, and compliance enforcement across complex
              payment flows.
            </h2>
          </div>

          {/* Right Side - Target Audiences */}
          <div className="space-y-2">
            {targetAudiences.map((audience, index) => (
              <div key={index} className="pb-2">
                <button
                  onClick={() => toggleAccordion(index)}
                  className="w-full flex items-start border-gray-200 border p-2 justify-between gap-4 text-left group"
                >
                  <h3
                    className={`text-lg font-bold leading-tight transition-colors ${
                      openAccordion === index
                        ? "text-[#17599d]"
                        : "text-gray-900"
                    }`}
                  >
                    {audience.title}
                  </h3>
                  <div className="shrink-0 mt-1">
                    {openAccordion === index ? (
                      <Minus
                        className="w-5 h-5 text-[#17599d]"
                        strokeWidth={3}
                      />
                    ) : (
                      <Plus className="w-5 h-5 text-gray-900" strokeWidth={3} />
                    )}
                  </div>
                </button>

                {/* Accordion Content */}
                {openAccordion === index && audience.description && (
                  <div className="mt-4 space-y-4">
                    <p className="text-gray-600 text-sm leading-relaxed">
                      {audience.description}
                    </p>
                  </div>
                )}
              </div>
            ))}
          </div>
        </div>
      </div>
    </section>
  );
}
